def intermedio(a, b):
    """
    Calcula el punto intermedio entre dos números.

    Args:
    a (float): El primer número.
    b (float): El segundo número.

    Returns:
    float: El punto intermedio entre a y b.
    """
    return (a + b) / 2

# Comprobar el punto intermedio entre -12 y 24
resultado = intermedio(-12, 24)
print("El punto intermedio entre -12 y 24 es:", resultado)
